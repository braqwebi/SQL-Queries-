# QUESTION 1 -Top 10 vaccines used (text search inside vaccine_used)

SELECT vaccine_used,
       COUNT(*) AS times_used
FROM vaccine
GROUP BY vaccine_used
ORDER BY times_used DESC
LIMIT 15;



# QUESTION 2Total vaccinations per year
SELECT YEAR(date_updated) AS year,
       SUM(total_vaccinations) AS yearly_total
FROM vaccine
GROUP BY YEAR(date_updated)
ORDER BY year;

#. Find all reports where vaccine_used IS NULL or empty
SELECT vaccine_id, date_updated, total_vaccinations
FROM vaccine
WHERE vaccine_used IS NULL OR vaccine_used = '';

#Get the minimum, maximum, and average vaccination count for 2022
SELECT MIN(total_vaccinations) AS min_2022,
       MAX(total_vaccinations) AS max_2022,
       AVG(total_vaccinations) AS avg_2022
FROM vaccine
WHERE YEAR(date_updated) = 2022;
