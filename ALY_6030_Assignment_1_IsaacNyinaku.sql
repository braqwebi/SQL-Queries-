CREATE TABLE vaccination_snapshot (
    snapshot_id        INT AUTO_INCREMENT PRIMARY KEY,
    country_id         INT NOT NULL,
    source_id          INT NOT NULL,
    vaccine_id         INT NOT NULL,
    date_updated       DATE NOT NULL,
    total_vaccinations INT,

    CONSTRAINT fk_snapshot_country FOREIGN KEY (country_id) REFERENCES country(country_id),
    CONSTRAINT fk_snapshot_source FOREIGN KEY (source_id) REFERENCES data_source(source_id),
    CONSTRAINT fk_snapshot_vaccine FOREIGN KEY (vaccine_id) REFERENCES vaccine(vaccine_id)
);
